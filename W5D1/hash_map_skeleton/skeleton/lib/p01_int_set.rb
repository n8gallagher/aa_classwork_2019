  end
end

class ResizingIntSet
  attr_reader :count
  attr_accessor :num_buckets, :store

  def initialize(num_buckets = 20)
    @store = Array.new(num_buckets) { Array.new }
    @count = 0
  end

  def insert(num)
    # @store += Array.new(num - num_buckets) { Array.new } if num > num_buckets
    resize! if @count == num_buckets
    if !self.include?(num)
        @store[num] = true 
        @count += 1
    end
  end

  def remove(num)
    if self.include?(num)
      @store[num] = false
      @count -= 1
    end
  end

  def include?(num)
    @store[num] == true
  end

  private

  def [](num)
    # optional but useful; return the bucket corresponding to `num`
  end

  def num_buckets
    @store.length
  end

  def resize!
    @store += Array.new(num_buckets) { Array.new }

  end
end
